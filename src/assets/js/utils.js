export function convertChars2Emoji(str) {
  if (!str) {
    return "";
  }
  return str.replace(
    new RegExp(
      Object.keys(CHAR2EMOJI)
        .join("|")
        .replace(/\[/g, "\\[")
        .replace(/\]/g, "\\]"),
      "g"
    ),
    key => CHAR2EMOJI[key]
  );
}
